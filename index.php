            return;
        }
        if (sValue === '') {
            list.show();
        } else {
            list.each(function() {
                var oLabel = $(this).find('label');
                if (oLabel.length > 0) {
                    if (oLabel.text().toLowerCase().indexOf(sValue.toLowerCase()) >= 0) {
                        $(this).show();
                    } else {
                        $(this).hide();
                    }
                }
            });
            lastinput = sValue;
        }
    });
}

